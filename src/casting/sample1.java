package casting;

public class sample1            //super class
 {
     public void car()
     {
    	 System.out.println("car :BMW");
     }
	
	public void home()
	{
		System.out.println("home:2bhk");
	}
	 
	//up casting :assigning subclass properties into super class is called as up casting 
	//first we have to perform inheritance to perform up casting
	
	//after performing inheritance properties of super class comes into the subclass then in subclass programmer can declare 
	//new properties
	//at the time of up casting operation the properties  inherited from the  superclass are only eligible for the up casting operation
	//the new properties declared inside subclass are not eligible for up casting operation
		
	
}
